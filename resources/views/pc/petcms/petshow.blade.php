                            <em>4</em>
                            <h2>金毛犬性格特点</h2></div>
                        <div class="entry_detail">
                            <p style="text-align:center">
                                <span class="replace_title">
                                    <img style="width:500px;height:350px;" ptype="1" src="{{$commonAssetUrl}}/petcms/m/img/9383b-1744.jpg" border="0" height="350" hspace="0" vspace="0" width="500" alt="金毛犬的性格特点" title="金毛犬的性格特点" />
                                    <br />
                                    <a href="http://www.boqii.com/tag/910/" target="_blank" style="text-decoration:none;border-bottom:1px dotted #FE730C;">金毛犬</a>(
                                    <a href='http://www.boqii.com/pet-all/332.html' target="_blank">详情介绍</a>)</span></p>
                            <p>　　在很多家庭中，人们对金毛犬的大概认识就是金毛犬性格活泼、爱撒娇、忠诚、憨厚，我么在玩耍的时候都可以看到他的踪影，他对任何人都友善，可以和人类成为好朋友，因为他的好脾气以及聪明的脑袋，现在已有很多金毛犬被训练成导盲犬，为人类做贡献。
                                <br />　　
                                <strong>性格特点
                                    <br />　　玩耍</strong>
                                <br />　　狗儿忙碌地不断将东西捡回来，最擅长的就是捡拖鞋、鞋子、球和布娃娃的等等。最喜欢的玩具是球形玩具。来到饲主的身边，抬起一条腿，吸引饲主的注意力，或是飞奔过来，和饲主撒娇，要求一起玩耍。会“哼、哼”的用鼻音撒娇，在饲主的周围不断绕圈子，或是一看到什么东西，就立刻咬在嘴里，跑向饲主；即使是
                                <br />　　一大块枯木也不放过。
                                <br />　　
                                <strong>撒娇</strong>
                                <br />　　发出“哼，哼”带着鼻音的撒娇声，身体不断靠近，希望饲主可以抚摸它。它会穿过饲主的跨下，或是露出肚子躺下向饲主“耍赖”。这种时候，不要凶巴巴的把它赶走，即使只有片刻，也努力和它保持肢体接触。这样可使它感受到饲主的爱。
                                <br />　　
                                <strong>寂寞</strong>
                                <br />　　刚离开母亲的小狗或是一直独自留在家里时，就会“呜~~呜~~”的叫着。垂着肩膀，低着头，全身无力的站在它的“地盘”。即使有球滚过来，也不会看一眼。“呼”的叹一个气，努力想要使自己睡一觉。这种时候，只有饲主的爱才能给它温柔。
                                <br />　　
                                <strong>服从</strong>
                                <br />　　狗对自己认同的首领会完全服从。爱犬的主人当然就是饲主，它只会对自己的主人做出仰躺，露出最大弱点的肚子这种毫无防备的动作，代表自己完全没有反抗的意思，是绝对服从的信号。另外，尾巴向后伸直，将腹部趴在地面，塌着耳朵，哀怨的抬头看着饲主时，代表服从的意思。
                                <br />　
                                <strong>　兴奋</strong>
                                <br />　　为了怕玩具弄丢了，会用前腿用力夹住玩具，或是用牙齿咬，摇晃。由于太兴奋，还会流口水或是挺起肚子。
                                <br />　
                                <strong>　满足</strong>
                                <br />　　在充分活动，玩耍后，就会懒洋洋地躺下，沉浸在
                                <a href='http://www.boqii.com/tag/3254/' target='_blank' style='text-decoration:none;border-bottom:1px dotted #FE730C;'>心情</a>愉快的疲劳感中，内心倍感满足。一边盯着饲主和家人的一举一动，确认大家并没有忘记它的存在。心情好的时候，还会发出愉悦地撒娇声。
                                <br />　
                                <strong>　喜悦</strong>
                                <br />　　吃饭和散步是快乐时光。垂下耳朵，眯起眼睛，吐出舌头是它在心情愉快时的表情。尾巴用力摇晃着，身体左右扭动，步履轻盈。尾巴拼命摇晃时，是它最高兴的时候。有时候，它会皱起鼻子，翘起上唇，做出微笑的表现。当它从鼻子中发出“哼，哼”声时，也是高兴的象征。
                                <br />　　
                                <strong>累了</strong>
                                <br />　　充分运动后的疲劳也令狗感到不堪负荷。小狗会立刻昏昏欲睡，打着哈欠，不一会儿就进入梦乡。当它深深入睡时，怎么叫，也叫不醒它，不妨让它好好的睡上一大觉。俗话说“一瞑大一寸”，当它好好的睡了一觉醒来后，又会精力充沛的四处活动，直到累了为止。
                                <br />　　
                                <strong>思考</strong>
                                <br />　　在思考时，狗也会沉默。但狗不会沉思，因为这不符合它的个性。它很快的就会投入下一个行动，并十分热衷其中。当它在行动与行动之间的瞬间进行思考，并不断重复这种思考后，它可以从中学习很多。因此，重复练习是调教的关键。
                                <br />　　
                                <strong>诉说</strong>
                                <br />　　当狗想要诉说什么时，会拼命用这种“欲言又止”的眼神抬头望着饲主。它会不厌其烦的做出相同的动作，进而发出低鸣的叫声，希望饲主能够了解它的心情。这个时候，应该努力从它的眼睛中察觉它的要求。狗的诉求都十分简单，纯朴，它绝对不可能提出奢侈的要求。
                                <br />　　
                                <strong>无聊</strong>
                                <br />　　狗之所以会感到无聊，是因为在玩得很尽兴后，暂时不知道自己接下来该做什么。于是，就觉得浑身懒洋洋的，只有眼睛不断寻找着新的调皮对象。但狗无法一直都沉浸在这种无聊中，只要一有刺激它好奇心的事物，就会立即起身，完全忘记刚才的自己。
                                <br />　
                                <strong>　兴趣正浓</strong>
                                <br />　　狗的好奇心十分旺盛。第一次看到动物和昆虫时。耳朵会敏感的竖起，尾巴不断摇晃，带着些许的紧张，慢慢靠近。嗅着味道，当了解“一切安全”后，就会用鼻子闻，用嘴巴咬……当感到奇怪或是遇到奇怪的东西时，会像人一样歪着脖子，陷入思考。
                                <br />　　
                                <strong>快乐</strong>
                                <br />　　饲主和自己一起玩耍时，会令它感到乐不可支。竖起尾巴，伸长脖子，轻快的一路小跑，高兴时，不停的跳跃，全身表现出无法克制的快乐。也会上下摇动耳朵，吐出舌头“哈，哈”地向饲主撒娇。
                                <br /></p></div>
