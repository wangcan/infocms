                            <h2>金毛犬形态特征</h2></div>
                        <div class="entry_detail">
                            <p style="text-align:center">
                                <span class="replace_title">
                                    <img style="width:500px;height:350px;" ptype="1" src="{{$commonAssetUrl}}/petcms/m/img/ba62d-6567.jpg" border="0" height="350" hspace="0" vspace="0" width="500" alt="金毛犬的形态特征" title="金毛犬的形态特征" />
                                    <br />
                                    <a href="http://www.boqii.com/tag/910/" target="_blank" style="text-decoration:none;border-bottom:1px dotted #FE730C;">金毛犬</a>(
                                    <a href='http://www.boqii.com/pet-all/332.html' target="_blank">详情介绍</a>)</span></p>
                            <p>　　金毛犬在犬的智商中排名在第四位，由于金毛犬有着一双温柔的眼睛、微笑的脸庞、天使般完美的性格、一身笼罩着天堂光辉的金色被毛，目前多被用作家庭犬及导盲犬。美国犬业俱乐部(American Kennel Club,AKC)标准规定其毛皮是“丰富的，有光泽的由不同层次的金色(rich，lustrous golden of various shades)”，不允许有过于浅或者过于深的颜色。
                                <br />　　它本身分为淡奶油(色素不够)及深咖啡二大类的颜色。
                                <br />　　金毛寻回犬主要的换毛期为一年二次，但由于体积较大毛量比小型犬多，常被误认为很会掉毛，无论大型犬或小型犬都会掉毛，平时的掉毛只是每天的新陈代谢而已，只要常常为它梳毛就可减少这困扰
                                <br />　　金毛是一个均称、有力、活泼的犬种，学名为
                                <a href='http://www.boqii.com/tag/2024/' target='_blank' style='text-decoration:none;border-bottom:1px dotted #FE730C;'>金毛寻回猎犬</a>，稳固、身体各部位配合合理，腿既不太长也不笨拙，表情友善，个性热情、机警、自信。因其是一种猎犬，在困苦的工作环境中才能
                                <a href='http://www.boqii.com/tag/5807/' target='_blank' style='text-decoration:none;border-bottom:1px dotted #FE730C;'>表现</a>出他的本质特点。他的总体外貌、平衡、步态和该犬种的用途应得到比身体任何一个单独部分更多的重视。
                                <br />　　
                                <strong>缺陷：</strong>任何与该犬种的理想描述相背离均可视为缺陷，缺陷的严重程度根据其与该犬种的用途相矛盾或与该犬种的特质相悖的程度决定。
                                <br />　　
                                <strong>失格条件</strong>
                                <br />　　1.身高低于或高于标准身高的2.5m。
                                <br />　　2.牙齿呈上腭式突出或下腭式突出。
                                <br />　　3.胸部有大量白毛或白斑。
                                <br /></p></div>
