                                                        <p>散发着香味的文具吸引着孩子，但是这些香味文具背后很可能是笨、甲醛等有害芳香胺在作怪。</p>
                                                        <p>
                                                            <br /></p>
                                                        <p>
                                                            <strong>纸张不要太白</strong></p>
                                                        <p>太白的纸张可能含荧光增白剂，摄入过多会对孩子视力、皮肤等产生危害，尤其是小学生和婴幼童。</p>
                                                        <p>
                                                            <br /></p>
                                                        <p>
                                                            <strong>不买鲜艳型文具</strong></p>
                                                        <p>不少颜色鲜艳的荧光笔，以及涂改液、橡皮中均含有酮类溶剂、苯类物质，进入人体内有可能会致癌。</p>
                                                        <p>
                                                            <br /></p>
                                                        <p>
                                                            <strong>环保无毒优先</strong></p>
                                                        <p>尽量在正规商场和商店购买儿童文具。选购标注“环保、无毒”等字样、产品名称、执行标准、生产厂名、厂址、警示用语、使用注意事项、生产日期、保质期等内容清楚的文具。</p>
