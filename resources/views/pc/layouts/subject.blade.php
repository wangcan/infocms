                                    <strong>文具用品税点</strong></p>
                                <p>增值税小规模纳税人销售办公用品适用的增值税税率是3%，应纳税额=销售额×征收。</p>
                                <p>【法律依据】</p>
                                <p>《中华人民共和国增值税暂行条例》第十二条，小规模纳税人增值税征收率为3%，国务院另有规定的除外。</p>
                                <p>《中华人民共和国增值税暂行条例》第十三条，小规模纳税人以外的纳税人应当向主管税务机关办理登记。具体登记办法由国务院税务主管部门制定。</p>
